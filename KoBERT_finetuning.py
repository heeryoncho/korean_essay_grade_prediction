dataset_train = nlp.data.TSVDataset("data/{}/train_{}.txt".format(folder, i), field_indices=[1,2], num_discard_samples=1)
dataset_val = nlp.data.TSVDataset("data/{}/val_{}.txt".format(folder, i), field_indices=[1,2], num_discard_samples=1)
dataset_test = nlp.data.TSVDataset("data/{}/test_{}.txt".format(folder, i), field_indices=[1,2], num_discard_samples=1)


